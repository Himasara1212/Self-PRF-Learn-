public class Operator {
  public static void main(String[] args) {
     
  // Increment -> (x++) & Decrements -> (x--)
  // x = 1;

  int x = 1;
  System.out.println(x); // 1

  x = x + 1 ;
  System.out.println(x); // 2

  x++;
  System.out.println(x); // 3

  x--;
  System.out.println(x); // 2

  }
}
